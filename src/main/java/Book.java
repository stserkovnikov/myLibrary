public class Book {
    int id;
    String Title;
    String Author;
    String Note;

    public void setId(int id) {
        this.id = id;
    }

    public void setTitle(String title) {
        Title = title;
    }

    public void setAuthor(String author) {
        Author = author;
    }

    public void setNote(String note) {
        Note = note;
    }


}
